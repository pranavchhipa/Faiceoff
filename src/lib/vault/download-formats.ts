/**
 * Generate a PDF package with:
 *   - Page 1: Image full-bleed with generation ID caption
 *   - Page 2: License details and brief metadata
 *
 * @returns A Buffer containing the PDF (magic bytes: %PDF)
 */
export async function generatePdfPackage(input: GenerateDownloadInput): Promise<Buffer> {
  const { imageUrl, generationId, brief = {} } = input;

  // Fetch image as base64 data URI for embedding
  const { buffer: imageBuffer, mimeType } = await fetchAsBuffer(imageUrl);
  const imageBase64 = `data:${mimeType};base64,${imageBuffer.toString('base64')}`;

  const briefEntries = Object.entries(brief).filter(
    ([k]) => !['pipeline_version', 'product_image_url'].includes(k),
  );

  const pdfDoc = createElement(
    PdfDocument,
    null,
    // Page 1: Image full-bleed
    createElement(
      Page,
      { size: 'A4', style: pdfStyles.imagePage },
      createElement(PdfImage, { src: imageBase64, style: pdfStyles.image }),
      createElement(
        View,
        { style: pdfStyles.captionContainer },
        createElement(
          Text,
          { style: pdfStyles.caption },
          `Faiceoff Generation ${generationId}`,
        ),
      ),
    ),
    // Page 2: License & brief
    createElement(
      Page,
      { size: 'A4', style: pdfStyles.certPage },
      createElement(Text, { style: pdfStyles.heading }, 'Faiceoff License Certificate'),
      createElement(Text, { style: pdfStyles.subheading }, 'AI Likeness Licensing Platform'),
      createElement(View, { style: pdfStyles.divider }),
      createElement(Text, { style: pdfStyles.label }, 'Generation ID'),
      createElement(Text, { style: pdfStyles.value }, generationId),
      createElement(Text, { style: pdfStyles.label }, 'Generated On'),
      createElement(Text, { style: pdfStyles.value }, new Date().toUTCString()),
      briefEntries.length > 0
        ? createElement(
            View,
            null,
            createElement(View, { style: pdfStyles.divider }),
            createElement(Text, { style: pdfStyles.subheading }, 'Generation Brief'),
            ...briefEntries.map(([key, value]) =>
              createElement(
                View,
                { key },
                createElement(Text, { style: pdfStyles.label }, key.replace(/_/g, ' ')),
                createElement(
                  Text,
                  { style: pdfStyles.value },
                  typeof value === 'string' ? value : JSON.stringify(value),
                ),
              ),
            ),
          )
        : null,
      createElement(View, { style: pdfStyles.divider }),
      createElement(Text, { style: pdfStyles.label }, 'License Terms'),
      createElement(
        Text,
        { style: pdfStyles.value },
        'This image is licensed for use under the Faiceoff Platform License Agreement. ' +
          'Use is restricted to the specified campaign. Commercial redistribution is prohibited ' +
          'without explicit written consent from the creator.',
      ),
      createElement(
        Text,
        { style: pdfStyles.footer },
        `© ${new Date().getFullYear()} Faiceoff — faiceoff.com`,
      ),
    ),
  );

  return renderToBuffer(pdfDoc);
}

