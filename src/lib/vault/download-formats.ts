/**
 * Fetch a remote URL and return it as a Buffer.
 * Handles both http(s) URLs and data: URIs.
 */
async function fetchAsBuffer(url: string): Promise<{ buffer: Buffer; mimeType: string }> {
  if (url.startsWith('data:')) {
    const semi = url.indexOf(';');
    const comma = url.indexOf(',');
    const mimeType = url.slice(5, semi) || 'application/octet-stream';
    const buffer = Buffer.from(url.slice(comma + 1), 'base64');
    return { buffer, mimeType };
  }

  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: HTTP ${response.status}`);
  }
  const arrayBuffer = await response.arrayBuffer();
  const mimeType = response.headers.get('content-type') ?? 'application/octet-stream';
  return { buffer: Buffer.from(arrayBuffer), mimeType };
}

/** Determine file extension from a URL or mime type. */
function imageExtension(url: string, mimeType?: string): string {
  // Try URL extension first
  const urlPath = url.split('?')[0];
  const ext = urlPath.split('.').pop()?.toLowerCase();
  if (ext && ['png', 'jpg', 'jpeg', 'webp', 'gif'].includes(ext)) return ext;

  // Fall back to MIME type
  const mimeMap: Record<string, string> = {
    'image/png': 'png',
    'image/jpeg': 'jpg',
    'image/webp': 'webp',
    'image/gif': 'gif',
  };
  return mimeMap[mimeType ?? ''] ?? 'png';
}

