/**
 * Generate a ZIP archive containing:
 *   - /image.{ext}       — the original generated image
 *   - /license-cert.pdf  — the license certificate (if certUrl provided)
 *   - /readme.txt        — attribution and license summary
 *
 * @returns A Buffer containing the ZIP file (magic bytes: PK\x03\x04)
 */
export async function generateOriginalZip(input: {
  imageUrl: string;
  certUrl?: string | null;
  generationId: string;
}): Promise<Buffer> {
  const { imageUrl, certUrl, generationId } = input;

  const zip = new JSZip();

  // Fetch and add the image
  const { buffer: imageBuffer, mimeType } = await fetchAsBuffer(imageUrl);
  const ext = imageExtension(imageUrl, mimeType);
  zip.file(`image.${ext}`, imageBuffer);

  // Fetch and add cert PDF if available
  if (certUrl) {
    try {
      const { buffer: certBuffer } = await fetchAsBuffer(certUrl);
      zip.file('license-cert.pdf', certBuffer);
    } catch (err) {
      // Cert fetch failure is non-fatal — log and continue without it
      console.warn(`[vault/zip] Failed to fetch cert for ${generationId}:`, err);
    }
  }

  // Add readme
  zip.file('readme.txt', buildReadmeText(generationId));

  const arrayBuffer = await zip.generateAsync({ type: 'arraybuffer' });
  return Buffer.from(arrayBuffer);
}

