/**
 * Generate a DOCX package with:
 *   - Title and brand info
 *   - Embedded image (as Base64 ImageRun)
 *   - Generation brief details
 *   - License terms text
 *
 * @returns A Buffer containing the DOCX (ZIP-based; magic bytes: PK\x03\x04 with [Content_Types].xml)
 */
export async function generateDocxPackage(input: GenerateDownloadInput): Promise<Buffer> {
  const { imageUrl, generationId, brief = {}, creator, brand } = input;

  // Fetch image for embedding
  const { buffer: imageBuffer, mimeType } = await fetchAsBuffer(imageUrl);

  // Determine image type for docx ImageRun
  const extToType = {
    png: 'png',
    jpg: 'jpg',
    jpeg: 'jpg',
    webp: 'png', // docx doesn't support webp, convert header — rasterized content should still show
    gif: 'gif',
  } as const;
  const ext = imageExtension(imageUrl, mimeType);
  const imageType = extToType[ext as keyof typeof extToType] ?? 'png';

  const briefEntries = Object.entries(brief).filter(
    ([k]) => !['pipeline_version', 'product_image_url'].includes(k),
  );

  const noBorder = {
    top: { style: BorderStyle.NONE, size: 0 },
    bottom: { style: BorderStyle.NONE, size: 0 },
    left: { style: BorderStyle.NONE, size: 0 },
    right: { style: BorderStyle.NONE, size: 0 },
    insideHorizontal: { style: BorderStyle.NONE, size: 0 },
    insideVertical: { style: BorderStyle.NONE, size: 0 },
  };

  const doc = new DocxDocument({
    sections: [
      {
        properties: {},
        children: [
          // Title
          new Paragraph({
            heading: HeadingLevel.TITLE,
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({
                text: 'Faiceoff — Licensed AI Image',
                bold: true,
                size: 48,
                color: '1a1513',
              }),
            ],
          }),

          // Subtitle
          new Paragraph({
            alignment: AlignmentType.CENTER,
            spacing: { before: 200, after: 400 },
            children: [
              new TextRun({
                text: 'AI Likeness Licensing Platform',
                size: 24,
                color: 'c9a96e',
              }),
            ],
          }),

          // Metadata table
          new Table({
            width: { size: 100, type: WidthType.PERCENTAGE },
            borders: noBorder,
            rows: [
              new TableRow({
                children: [
                  new TableCell({
                    borders: noBorder,
                    children: [
                      new Paragraph({
                        children: [new TextRun({ text: 'Generation ID', bold: true })],
                      }),
                    ],
                    width: { size: 30, type: WidthType.PERCENTAGE },
                  }),
                  new TableCell({
                    borders: noBorder,
                    children: [new Paragraph({ children: [new TextRun({ text: generationId })] })],
                  }),
                ],
              }),
              ...(creator
                ? [
                    new TableRow({
                      children: [
                        new TableCell({
                          borders: noBorder,
                          children: [
                            new Paragraph({
                              children: [new TextRun({ text: 'Creator', bold: true })],
                            }),
                          ],
                          width: { size: 30, type: WidthType.PERCENTAGE },
                        }),
                        new TableCell({
                          borders: noBorder,
                          children: [
                            new Paragraph({
                              children: [
                                new TextRun({
                                  text: creator.display_name,
                                }),
                              ],
                            }),
                          ],
                        }),
                      ],
                    }),
                  ]
                : []),
              ...(brand
                ? [
                    new TableRow({
                      children: [
                        new TableCell({
                          borders: noBorder,
                          children: [
                            new Paragraph({
                              children: [new TextRun({ text: 'Brand', bold: true })],
                            }),
                          ],
                          width: { size: 30, type: WidthType.PERCENTAGE },
                        }),
                        new TableCell({
                          borders: noBorder,
                          children: [
                            new Paragraph({
                              children: [new TextRun({ text: brand.company_name })],
                            }),
                          ],
                        }),
                      ],
                    }),
                  ]
                : []),
              new TableRow({
                children: [
                  new TableCell({
                    borders: noBorder,
                    children: [
                      new Paragraph({ children: [new TextRun({ text: 'Date', bold: true })] }),
                    ],
                    width: { size: 30, type: WidthType.PERCENTAGE },
                  }),
                  new TableCell({
                    borders: noBorder,
                    children: [
                      new Paragraph({
                        children: [new TextRun({ text: new Date().toUTCString() })],
                      }),
                    ],
                  }),
                ],
              }),
            ],
          }),

          // Spacer
          new Paragraph({ spacing: { before: 400 } }),

          // Image section heading
          new Paragraph({
            heading: HeadingLevel.HEADING_1,
            children: [new TextRun({ text: 'Generated Image', bold: true, color: '1a1513' })],
            spacing: { before: 400, after: 200 },
          }),

          // Embedded image
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new ImageRun({
                data: imageBuffer,
                transformation: { width: 480, height: 480 },
                type: imageType as 'png' | 'jpg' | 'gif',
              }),
            ],
          }),

          // Brief section
          ...(briefEntries.length > 0
            ? [
                new Paragraph({
                  heading: HeadingLevel.HEADING_1,
                  children: [
                    new TextRun({ text: 'Generation Brief', bold: true, color: '1a1513' }),
                  ],
                  spacing: { before: 400, after: 200 },
                }),
                new Table({
                  width: { size: 100, type: WidthType.PERCENTAGE },
                  borders: noBorder,
                  rows: briefEntries.map(
                    ([key, value]) =>
                      new TableRow({
                        children: [
                          new TableCell({
                            borders: noBorder,
                            children: [
                              new Paragraph({
                                children: [
                                  new TextRun({
                                    text: key.replace(/_/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase()),
                                    bold: true,
                                  }),
                                ],
                              }),
                            ],
                            width: { size: 30, type: WidthType.PERCENTAGE },
                          }),
                          new TableCell({
                            borders: noBorder,
                            children: [
                              new Paragraph({
                                children: [
                                  new TextRun({
                                    text: typeof value === 'string' ? value : JSON.stringify(value),
                                  }),
                                ],
                              }),
                            ],
                          }),
                        ],
                      }),
                  ),
                }),
              ]
            : []),

          // License terms
          new Paragraph({
            heading: HeadingLevel.HEADING_1,
            children: [new TextRun({ text: 'License Terms', bold: true, color: '1a1513' })],
            spacing: { before: 400, after: 200 },
          }),
          new Paragraph({
            children: [
              new TextRun({
                text:
                  'This image was generated under the Faiceoff Platform License Agreement. ' +
                  'Use is restricted to the campaign for which this generation was created. ' +
                  'Commercial redistribution, sublicensing, or use outside the agreed scope ' +
                  'is prohibited without explicit written consent from the creator. ' +
                  'Full terms are available at faiceoff.com/legal/license-terms.',
              }),
            ],
          }),

          // Footer
          new Paragraph({
            alignment: AlignmentType.CENTER,
            spacing: { before: 600 },
            children: [
              new TextRun({
                text: `© ${new Date().getFullYear()} Faiceoff — faiceoff.com`,
                color: '888888',
                size: 16,
              }),
            ],
          }),
        ],
      },
    ],
  });

  const arrayBuffer = await Packer.toBuffer(doc);
  return Buffer.from(arrayBuffer);
}
